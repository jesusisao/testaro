export const createNumroInstances = (
  familyName: string,
  familyNameKana: string,
  num: number
): Array<Human> => {
  const result = [];
  for (let i = 1; i <= num; i++) {
    result.push({
      familyName: familyName,
      familyNameKana: familyNameKana,
      ...Numros[i],
      sex: "male" as Sex
    });
  }
  return result;
};
