export const createNumroInstances = (
  familyName: string,
  familyNameKana: string,
  familyNameRome: string,
  num: number
): Array<Human> => {
  const result = [];
  for (let i = 1; i <= num; i++) {
    result.push({
      familyName: familyName,
      familyNameKana: familyNameKana,
      familyNameRome: familyNameRome,
      ...Numros[i],
      sex: "male" as Sex,
      birthday: new Date()
    });
  }
  return result;
};
